DECLARE

CURSOR DEL_CUR IS
	SELECT /*+ parallel(8) */ SCARTO.ROWID AS SCARTO_ROWID, 
			SCARTO.*, 
			TMP.ROWID AS TMP_ROWID
	FROM TBL_SCARTI_TMP_PFSALDI_MOVIMP SCARTO
	LEFT JOIN TMP_PFSALDI_MOVIMP TMP
		ON( TMP.CODICEINTERNO = SCARTO.CODICEINTERNO
			AND TMP.CODICERAPPORTO = SCARTO.CODICERAPPORTO)
	WHERE SCARTO.RIPROPONIBILE = 'S';

I 				NUMBER(38,0):=0;
REUSE_COUNT		NUMBER(38,0):=0;	


BEGIN

	FOR CUR_ITEM IN DEL_CUR 

		LOOP
			
		I := I+1;
		
		IF(CUR_ITEM.TMP_ROWID IS NULL) THEN
		
		INSERT /*+ append nologging parallel(8) */
				INTO TMP_PFSALDI_MOVIMP (	CODICEBANCA,
											CODICEAGENZIA,
											CODICERAPPORTO,
											CODICEINTERNO,
											DATASALDO,
											TIPORAPPORTO,
											DATASOTTOSCRIZIONE,
											DATASCADENZA,
											QUANTITA,
											CONTROVALORE,
											CODICERISCHIO,
											PREZZO_MEDIO_CARICO,
											CAMBIO_MEDIO_CARICO,
											CONTROVALORE_VINCOLATO
										)
								VALUES (CUR_ITEM.CODICEBANCA,
										CUR_ITEM.CODICEAGENZIA,
										CUR_ITEM.CODICERAPPORTO,
										CUR_ITEM.CODICEINTERNO,
										CUR_ITEM.DATASALDO,
										CUR_ITEM.TIPORAPPORTO,
										CUR_ITEM.DATASOTTOSCRIZIONE,
										CUR_ITEM.DATASCADENZA,
										to_number(CUR_ITEM.QUANTITA,'999999999999999999999999999.999999999999999999999999999'),    
										to_number(CUR_ITEM.CONTROVALORE,'999999999999999999999999999.999999999999999999999999999'),    
										CUR_ITEM.CODICERISCHIO,
										to_number(CUR_ITEM.PREZZO_MEDIO_CARICO,'999999999999999999999999999.999999999999999999999999999'),    
										to_number(CUR_ITEM.CAMBIO_MEDIO_CARICO,'999999999999999999999999999.999999999999999999999999999'),    
										to_number(CUR_ITEM.CONTROVALORE_VINCOLATO,'999999999999999999999999999.999999999999999999999999999')   
								);
										
				REUSE_COUNT := REUSE_COUNT + 1;			
				
			END IF;		
			
			DELETE /*+ nologging */ FROM TBL_SCARTI_TMP_PFSALDI_MOVIMP TBL
			WHERE TBL.ROWID = CUR_ITEM.SCARTO_ROWID;
						
		IF MOD(I,10000) = 0 THEN
			INSERT INTO OUTPUT_PRINT_TABLE VALUES (TO_NUMBER(TO_CHAR(SYSDATE,'YYYYMMDDHH24MISS')) || ' REUSE SCARTI TMP_PFSALDI_MOVIMP. COMMIT ON ROW: ' || I);
			COMMIT;
		END IF;

		END LOOP;
			INSERT INTO OUTPUT_PRINT_TABLE VALUES (TO_NUMBER(TO_CHAR(SYSDATE,'YYYYMMDDHH24MISS')) || ' REUSE SCARTI TMP_PFSALDI_MOVIMP. RECORD RECUPERATI: ' || REUSE_COUNT);
		COMMIT;

END;