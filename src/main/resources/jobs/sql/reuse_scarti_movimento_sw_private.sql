DECLARE

CURSOR DEL_CUR IS
	SELECT /*+ parallel(8) */ SCARTO.ROWID AS SCARTO_ROWID, 
			SCARTO.*, 
			TMP.ROWID AS TMP_ROWID
	FROM TBL_SCARTI_TMP_PFMOV_SWPRIVATE SCARTO
	LEFT JOIN TMP_PFMOV_SWPRIVATE TMP
		ON( TMP.NUMERO_POLIZZA = SCARTO.NUMERO_POLIZZA
			AND TMP.FONDO = SCARTO.FONDO
			AND TMP.DATA_NAV = SCARTO.DATA_NAV
			AND TMP.TIPO_MOVIMENTO = SCARTO.TIPO_MOVIMENTO 
			AND TMP.COD_LINEA = SCARTO.COD_LINEA
			AND TMP.COD_UNIVERSO = SCARTO.COD_UNIVERSO
			)
	WHERE SCARTO.RIPROPONIBILE = 'S';

I 				NUMBER(38,0):=0;
REUSE_COUNT		NUMBER(38,0):=0;	


BEGIN

	FOR CUR_ITEM IN DEL_CUR 

		LOOP
			
		I := I+1;
		
		IF(CUR_ITEM.TMP_ROWID IS NULL) THEN
		
		INSERT /*+ append nologging parallel(8) */
				INTO TMP_PFMOV_SWPRIVATE (	NUMERO_POLIZZA,
											FONDO,
											DATA_NAV,
											ISIN,
											TIPO_MOVIMENTO,
											NAV,
											QUOTE,
											IMPORTO,
											COD_LINEA,
											DATA_MOV,
											COD_UNIVERSO,
											COSTO_ETF
										)
								VALUES (CUR_ITEM.NUMERO_POLIZZA,
										CUR_ITEM.FONDO,
										CUR_ITEM.DATA_NAV,
										CUR_ITEM.ISIN,
										CUR_ITEM.TIPO_MOVIMENTO,
										TO_NUMBER(CUR_ITEM.NAV, '999999999999999999999999999.999999999999999999999999999'),
										TO_NUMBER(CUR_ITEM.QUOTE, '999999999999999999999999999.999999999999999999999999999'),
										TO_NUMBER(CUR_ITEM.IMPORTO, '999999999999999999999999999.999999999999999999999999999'),
										CUR_ITEM.COD_LINEA,
										CUR_ITEM.DATA_MOV,
										CUR_ITEM.COD_UNIVERSO,
										TO_NUMBER(CUR_ITEM.COSTO_ETF, '999999999999999999999999999.999999999999999999999999999')
								);
										
				REUSE_COUNT := REUSE_COUNT + 1;			
				
			END IF;		
			
			DELETE /*+ nologging */ FROM TBL_SCARTI_TMP_PFMOV_SWPRIVATE TBL
			WHERE TBL.ROWID = CUR_ITEM.SCARTO_ROWID;
						
		IF MOD(I,10000) = 0 THEN
			INSERT INTO OUTPUT_PRINT_TABLE VALUES (TO_NUMBER(TO_CHAR(SYSDATE,'YYYYMMDDHH24MISS')) || ' REUSE SCARTI TMP_PFMOV_SWPRIVATE. COMMIT ON ROW: ' || I);
			COMMIT;
		END IF;

		END LOOP;
			INSERT INTO OUTPUT_PRINT_TABLE VALUES (TO_NUMBER(TO_CHAR(SYSDATE,'YYYYMMDDHH24MISS')) || ' REUSE SCARTI TMP_PFMOV_SWPRIVATE. RECORD RECUPERATI: ' || REUSE_COUNT);
		COMMIT;

END;